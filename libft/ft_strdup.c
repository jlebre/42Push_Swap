#include "libft.h"

char	*ft_strdup(const char *str)
{
	char	*new_str;
	int	i;

	new_str = (malloc(sizeof(char) * (ft_strlen(str) + 1)));
	if (!new_str)
		return (NULL);
	i = 0;
	while (str[i])
	{
		new_str[i] = str[i];
		i++;
	}
	new_str[i] = '\0';
	return (new_str);
}
